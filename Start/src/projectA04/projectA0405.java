package projectA04;

public class projectA0405 {

	public static void main(String[] args) {
		int x = 10;
		int y = 20;
		
		if(x > 20 && y > 10) {
			System.out.println("20이상입니다.");
		}else if (x > 20 && y > 15) {
			System.out.println("25이상입니다.");
		}else if(x > 30 && y > 20) {
			System.out.println("30이상입니다.");
		}else {
			System.out.println("다른 숫자입니다.");
		}
		
		
	}

}
