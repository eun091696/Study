package projectA02;

public class projectA0201 {

	public static void main(String[] args) {
		int myBirthYear = 1996;
		int 내출생년도 = 1996;
		int intValue = 1000;
		System.out.println(myBirthYear);
		System.out.println(내출생년도);
		System.out.println(intValue);
		
		int intdata = 2147483647;
		System.out.println(intdata);

		byte byteval = 127;
		System.out.println(byteval);
		byte byteval1 = -128;
		System.out.println(byteval1);
		float floatval = 1.12f; //1.12뒤에 f가 없으면 double형태라 f를 반드시 붙여야 한다.
		System.out.println(floatval);
		double doubleval = 1.12;
		System.out.println(doubleval);
		
		//상수 (변하지 않는 값) final
		final double  PI = 3.14;
		System.out.println(PI);
		
		short shortval = 32767;
		System.out.println(shortval);
	}
}
